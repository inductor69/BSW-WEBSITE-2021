                        <div class="cd-faq-content">
                            <p>Grading basically depends on the course coordinator. In most of the cases relative
                                grading is done, i.e. depending on the score of whole class, Professor decides what
                                grade is to be given for what total. Usually a grade point of 7 is given if a student
                                obtains average (mean marks of the whole class) marks.
                                The minimum marks for award of an ‘A’ grade or 10 grade point is 80 % and the minimum
                                marks for award of ‘D’ grade or minimum passing grade is 30 %. However, it may vary
                                according to the Professor.<br>
                                Consider an example, In a particular course if professor sets 9 grade point at a score
                                of 65 and 10 at a score of 75 then students who have scored between 65 and 75 get 9 and
                                above 75 get 10.</p>
