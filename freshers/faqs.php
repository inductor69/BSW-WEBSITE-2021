                        <a class="cd-faq-trigger" href="#0">What is the concept of credits?</a>
                        <div class="cd-faq-content">
                            <p>Every course you complete has a specific number of credits assigned to it, which depend
                                on the number of hours of the lectures, labs and tutorials for that course. <br>
                                Lectures and Tutorials : One lecture or tutorial hour per week over the period of one 14
                                week semester is assigned one credit.<br>
                                Practical/Practice : One laboratory / practice hour per week over the period of one 14
                                week semester is assigned half credit.<br>
                                On obtaining a pass grade, the student accumulates the course credits as earned credits.
                                All programmes are defined by a certain total credit requirement.</p>
                        </div> <!-- cd-faq-content -->
                    </li>
                    <li>
                        <a class="cd-faq-trigger" href="#0">What is CGPA and SGPA?</a>
                        <div class="cd-faq-content">
                            <p>SGPA is the Semester Grade Point Average. Points secured in a semester =Σ (Course credits
                                × Grade point for all courses in which pass grade has been obtained)<br>
                                SGPA = Points secured in the semester/Credits registered in the semester(excluding
                                non-graded courses)<br>
                                CGPA is the Cumulative Grade Point Average and your CGPA is calculated from your SGPA of
                                the semesters completed till date.
                            </p>
                        </div> <!-- cd-faq-content -->
                    </li>
