  <h3 style="font-size: 35px;"><b>Life in Hostel</b></h3>
 
<strong style="font-size: 22px;">INTRODUCTION:  </strong><br>   
<p>It is said that home is sweet. But even then, life at home cannot be compared to your life at hostel. </p>
<p>
There is a vast difference between living in a secured and protected environment, with your family for almost 18 years and in a free and independent environment, with no familiar faces. It is a sudden jump, and initially, you may feel homesick sometimes and dislike the surroundings, but eventually, as you make new friends and adapt to the atmosphere, you will live some of the best days of your lives there, considering you don’t misuse the ample amount of freedom you have and indulge in rough activities.
</p>
With so many activities, events, competitions and festival celebrations going on in the hostel throughout the year, the atmosphere becomes vibrant and full of life. Not only do you develop a number of connections, but also become smart, active, disciplined and self-dependent. This is the phase of life when you transform from a kid to a man. So choose your path carefully, be in the right company of people and enjoy the pleasant journey you are going to be a part of.  <br><br><br><br><br><br><br>
 
<strong style="font-size: 22px;">HOSTEL REPRESENTATIVES AND SECRETARIES:  </strong><br>
<p>Every hostel at IITD has a number of positions of responsibility for the students residing in that hostel. </p>
<p>For instance, all the cultural activities taking place in the hostel are supervised by the Cultural Secretary, who is a third year student, and assisting him in his work are hostel representatives of each BRCA club, like music, dance, drama, photography, debating etc., who are second year students, likewise all sports events are managed by the Sports Secretary. 
</p><p>
Other than this, there’s a Mess Secretary (third-year student), who handles the issues related to the mess and food and a Maintenance Secretary, again a thirdie, who is responsible for the proper working of the common areas and the hostel maintenance. 
</p><p>
All these secretaries are supervised and managed by the House Secretary (fourth-year student). To oversee hostel funds and other hostel related matters, each hostel has a Warden and a House Master, who are professors in the institute. More details will be given once you reach here. </p><br><br><br>
 
<strong style="font-size: 22px;">ACTIVTIES, EVENTS AND HOUSE DAY: </strong><br>
<p>
There are a number of activities and events held in the hostels throughout the year, both inter and intra. Events related to each and every extra-curricular activity are held regularly and students of all years are encouraged to participate in them.
</p><p>
So if you have a talent, it won’t get unnoticed. Other than this, every hostel celebrates its annual House Day, to give the passing out batch a memorable farewell, in which the entire hostel is decorated and a number of activities take place.</p><p>Moreover, it is the only day of the year when girls are allowed in boys’ hostel and vice-versa. So House Day is one day which you don’t want to miss. </p><br><br><br><br>
 
