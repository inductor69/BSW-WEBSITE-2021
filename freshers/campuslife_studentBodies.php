<h3 style="font-size: 40px;"><b>Student bodies at IIT-Delhi</b></h3><br>
<div id="studentBodies"></div>
<p><strong style="font-size: 30px;">BSW</strong>
- This board is the welfare board of the students and for the students. It works for the benefits, rights and betterment of the student fraternity. <a href="http://smp.iitd.ac.in/" target="_blank"><b>Student Mentorship Program(SMP) </b></a>and <a href = "http://bsw.iitd.ac.in/counselling.php" target="_blank"><b>Student Counselling Services(SCS)</b></a>  are an integral part of the board not only help freshers in adapting the new ambience and solving their issues thereafter; but works to ease the stay of each and every student (UGs and PGs both) here at IITD.Feel free to reach out your mentors or BSW reps for any help in terms of academics , management and anything else bothering.<br>
You can follow us on <a href="https://www.facebook.com/boardforstudentwelfare/" target="_blank"><b>Facebook</b></a> and get more info on our <a href="http://bsw.iitd.ac.in" target="_blank"><b>website</b></a>.
<br>
<br><div class="parallaxbsw"></div><br><br>

<strong style="font-size: 30px;">BHM</strong>
This board is responsible for maintenance of hostel infrastructure, mess conduct and other amenities. House day to bid adieu to the outgoing seniors and cherish with them, their memories of stay here is celebrated with great pomp and show in all the hostels. Hostel fests and mess fess are also fun and happening. The annual grand BHM night is much awaited as trophies gets distributed and the dinner is relished. All the hostels put up stalls on the <b>BHM night </b>. Students, professors and the director, are invited on the occasion.<br>
For latest updates related to hostels and food outlets, follow BHM on <a href="https://www.facebook.com/BoardforHostelManagementIITDelhi/" target="_blank"><b>Facebook</b></a> and get more info on <a href="http://bhm.iitd.ac.in" target="_blank"><b>BHM website</b></a>.
         <br><br><div class="parallaxbhm"></div><br><br>

<strong style="font-size: 30px;">BRCA</strong>
The umbrella board meant for all the recreational and cultural activities comprises of several clubs under it-Dance, Dramatics, Debating, Fine Arts and Crafts, Literary, Music, Quizzing and Hindi Samiti.Adding spices to the monotonous academics, these clubs organise various events and workshops to cater to the overall development of the student. The much awaited, glamorous and hot-shot <b>‘Rendezvous’ </b>- the annual cultural fest of IIT Delhi, is also organized by BRCA.
<br>
To know more about cultural clubs in IIT Delhi, visit the <a href = "http://brca.iitd.ac.in" target="_blank"><b>website</b></a> and follow BRCA on <a href = "https://www.facebook.com/cultureIITD/" target="_blank"><b>Facebook</b></a>
<br><br><div class="parallaxbrca"></div><br><br>

<strong style="font-size: 30px;">BSA</strong>
This is the board regulating the sports activities of the institute. There are facilities for many sports in the campus like Aquatics, Athletics, Badminton, Basketball, Cricket, Football, Hockey, Lawn Tennis, Weight-Lifting, and many more. The institute’s swimming pool is known for its supreme quality at par with the Olympics level. Every year students are trained to represent IIT Delhi at inter IIT sports competition. Various inter-hostel tournaments are conducted round the year. <b>‘Sportech’ </b>-the annual sports festival of IIT Delhi is organised by the board.        <br>
<b><a href="http://bsw.iitd.ac.in/bsa.php" style="color: rgb(221,11,56);">Click here</a></b> for more information about this board and the facilities provided for sports in IIT Delhi.<br>
<br><div class="parallaxbsa"></div><br><br>

<strong style="font-size: 30px;">BSP</strong>
This Board is IIT Delhi's electronic and print media body, run entirely by the student community. It produces a variety of publications, from newsletters to magazines. Journalists report major events within campus and conduct research on important issues to write articles. Technical editors ensure the upkeep of the BSP website. It also provides a platform to contribute your own creations in the form of poems, stories, caricatures, blogs and much more. <b>‘Literati’ </b>-the annual literary fest of IIT Delhi is celebrated each year by the board.      <br>
Follow BSP on <a href="https://www.facebook.com/bspiitd/" target="_blank"><b>Facebook</b></a> and <a href="http://bsp.iitd.ac.in" target="_blank"><b>website</b></a> for more blogs and articles by BSP.<br>
<br><div class="parallaxbsp"></div><br><br>

<strong style="font-size: 30px;">CAIC</strong>
It stands for Co-curricular and Academic Interaction Council. It is an umbrella body of all the co-curricular clubs and departmental societies in IIT Delhi. It works primarily to promote the academic ambience of IIT Delhi. It acts as an interface between the administration and students for the implementation of new academic and co-curricular initiatives. It has its representatives in every department of IIT (both UG and PG). North India’s largest technical fest, IIT Delhi’s own <b>Technovanza Tryst </b> is annually organised by the board.        <br>
<br>
To know more about technical clubs and departments in IIT Delhi, visit the <a href = "http://caic.iitd.ac.in" target="_blank"><b>website</b></a> and follow CAIC on <a href = "https://www.facebook.com/caiciitd/" target="_blank"><b>Facebook</b></a>

<br><br><div class="parallaxcaic"></div><br><br>

<strong style="font-size: 30px;">SAC</strong>
Student Affairs Council is the overarching body monitoring the functioning of the 5 major boards: BRCA, BSA, BSW, BHM and BSP. Having student representatives as its members, it acts as the bridge between the student community and the administration, anchoring policy decisions that affect every aspect of Student life in the campus from academics, cultural activities to sports and hostel life. Issues concerning any of the constituent boards or otherwise can be addressed to <b>SAC representatives </b> for swift recourse.<br>
For updated info, follow SAC on <a href="https://www.facebook.com/saciitd/" target="_blank"><b>Facebook</b></a> and <a href="http://sac.iitd.ac.in" target="_blank"><b>website</b></a>.
<br><br><div class="parallaxsac"></div><br><br>

