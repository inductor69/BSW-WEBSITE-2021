<strong style="font-size: 22px;">Stationery, books, xerox :</strong><br>
<ul>
<li>
<b>Stationery items </b> like notebooks, record books, file-folders, journal sheets, chart papers, rulers/set squares/protractors, pens, calculators as well as art & craft materials like paints, charts, etc, are available <b>at SCOOPS </b>- a store run by BSW, located in the insti-area near the WindT. You also get services like photocopy, printing, lamination, binding,etc done here. <br>
</li>
<li> 
You can also get xerox, printout, etc done at the shop located on the <b>first floor of the library </b>or at your hostel shop( most hostels have these xerox facilities ). If you're looking for color or A3 sized printouts, library is the place for you. <br>
</li>
<li>
You can buy course related textbooks from the <b>‘Book Camp’ </b>organised by BSW. <br>
</li>
<li>
You can buy books from shops in <b>Ber Sarai </b>, which is about a 10 min walk from campus. You can get all kinds of course-related books and novels at a discounted rate or even second hand copies of these books here.  <br><br>
</li>
</ul>


<strong style="font-size: 22px;">Bank and ATM services : </strong><br>
<ul>
<li>There are two banks operating inside <b>IITD: SBI and Canara Bank.</b> SBI bank is located near the Lecture hall complex and Canara bank is located on the first floor of the main building, near UG section. <br>
</li>
<li>
There are three SBI atms in the campus – near SBI bank, in the Neelkanth shopping complex and Nalanda apartments area. There’s a Canara bank atm located opposite Nescafe, below the library and two IndiCash atms – one near Karakoram hostel and the other near Udaigiri Hostel.</ul>
<br><br>

 <strong style="font-size: 22px;">Medical Facilities :</strong><br>
We have our own hospital here in campus, which is<b> open 24X7 </b> . During the orientation week, you will all be given a <b>medical booklet </b>which will guarantee you free of charge treatment and medication at the IIT Delhi Hospital. In case of an emergency there is an ambulance service available to take you from your hostel to the hospital. You can call them directly or approach the nearest security guard to summon them. In case of an emergency which cannot be handled by the staff at the IIT hospital at that time, the students are <b>referred to AIIMS </b> or Safdarjung Hospital which are located very close to the campus. The IIT ambulance will drop you there free of cost. If you wish to go for some advanced procedures you can go to hospitals like Rockland, Max, and Orthonova. You need not worry about the expenditure, as every student at IITD is insured for Rs. 1 Lakh (with Buffer upto Rs. 5 Lakh) + Rs. 1 Lakh (for accident victims) + additional help in the form of <b>“Student Distress Fund”</b>. A detailed information is provided in the BSW diary regarding all the hospitals nearby along with their contact information.<br><br>
 
 
<strong style="font-size: 22px;">Eateries :</strong><br>
<ul>
<li>
<b>Insti-area</b> - There is a cafeteria near the central workshop , and many shops like Amul, Nescafe, HPMC and Cafe Coffee Day in the institute area. There’s also a snacks shop next to SCOOPS.<br>
</li>
<li>
<b>Rajdhani Food Center </b>: In front of Aravali hostel, there’s Rajdhani food center where you can get a variety of meals, snacks, juices and shakes.<br>
</li>
<li>
<b>Nirvaha Cafe, Bakery and Juice Center</b> - Near Zanskar hostel, you can find a juice center and Nirvaha cafe, which provides organic food. Also, there’s a bakery there, where you can get samosa, patties and other snacks at cheap prices.<br> 
</li>
<li>
<b>Mother Dairy and Safal Stores </b>: There are three Mother Dairy outlets – near Nilgiri hostel, Kumaon hostel and one at the girls’ hostel gate. Also, there are two Safal outlets where you can buy fruits and vegetables- near Nilgiri hostel and girls’ hostel gate. <br>
</li>
<li>
<b>Night Messes </b>- Almost all the hostels have their own night mess, where you get inexpensive food. Be it snacks or late night dinner, these are the places to go. There are juice & shake center in Aravali and Kumaon hostel which are open until late at night. <br>
</li>
<li>
<b>Outside campus </b>- One cannot miss out on exploring restaurants and food joints with friends, especially when you are in a city like Delhi :P. The list is enormous and depends on person to person but here are some places you would definitely want to try out during your stay here. There is <b>Satya Niketan</b> with some amazing cafes which are quite pocket friendly too. Then there’s <b>SDA</b>, right across our campus, so you can always go there without much prior thought. You can also try out the food outlets in <b>Green Park</b>, <b>Ber Sarai</b>, <b>Khan Market</b>, <b>Connaught Place</b>, <b>Chandni Chowk</b>, etc. Also, you can always ask your seniors for suggestions, they’ll give you plenty of options as per your tastes. Feeling lazy? You can try sites/apps like Zomato, Swiggy, Faasos, Foodpanda or TastyKhaana and order food.
 </li>
</ul>
<br><br>
<strong style="font-size: 22px;">Photo studio : </strong><br>
There’s a photo studio behind the Juice Centre, where you can get your passport size photo clicked and printed.<br><br>
 
<strong style="font-size: 22px;">Railway reservation counter : </strong><br>
There is a railway reservation counter besides Jwalamukhi hostel where you can book railway tickets in Tatkal by just showing your id card.<br><br>
 
<strong style="font-size: 22px;">Security control room : </strong><br>
For all the complaints regarding cycle theft or any security concern, there is a security control room located at in front of the biotech lawns in MS building.<br><br>
<strong style="font-size: 22px;">Post Office: </strong><br>
There’s a post office located in the Neelkanth shopping complex. <br><br>
